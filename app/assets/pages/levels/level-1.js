function calculateCodes(code, min, max) {
  // initial value of counter
  // // evaluation
  // // // what should happen to counter
  var array = [];
  for (var i = min; i <= max; i = i + 5) {
    var smash = code + i;
    array.push(smash);
  }
  return array;
};

