function calculateCodes(code, min, max) {
  // initial value of counter
  // // evaluation
  // // // what should happen to counter
  var array = [];
  for (var i = min; i <= max; i = i + 5) {
    var smash = code + i;
    array.push(smash);
  }
  return array;
};

function findObjects(objArray) {
  for (var i = 0; i < objArray.length; i++) {
    var tempObj = objArray[i];
    // var tempSection = tempObj['section'];
    // var tempTitle = tempObj['title'];
    var codeArray = calculateCodes(tempObj['code'], tempObj['minTempo'], tempObj['maxTempo']);
    console.log(codeArray);
    createProgressModule(tempObj['section'], tempObj['title'], tempObj['link'], codeArray);
    function createProgressModule(section, title, link, array) {

      // create elements
      var mainDiv = document.createElement("div");
      var sectionDiv = document.createElement("div");
      var titleDiv = document.createElement("div");
      var tempoDiv = document.createElement("div");
      var linkDiv = document.createElement("div");

      var sectionP = document.createElement("p");
      var titleP = document.createElement("p");
      var linkA = document.createElement("a");

      var sectionNode = document.createTextNode(section);
      var titleNode = document.createTextNode(title);
      var linkNode = document.createTextNode("Go");

      // create ids
      var sectionId = i + "-section";
      var titleId = i + "-title";
      var tempoId = i + "-tempo";
      var linkId = i + "-link";

      // set id's and classes
      mainDiv.setAttribute("id", section);
      mainDiv.setAttribute("class", "progress-module");
      sectionDiv.setAttribute("id", sectionId);
      sectionDiv.setAttribute("class", "progress-section");
      titleDiv.setAttribute("id", titleId);
      titleDiv.setAttribute("class", "progress-title");
      tempoDiv.setAttribute("id", tempoId);
      tempoDiv.setAttribute("class", "progress-tempo");
      linkDiv.setAttribute("id", linkId);
      linkDiv.setAttribute("class", "progress-link");
      linkA.setAttribute("href", "../" + section + "/" + link + ".html");
      // tempoP.setAttribute("id", tempoPId);

      for (y = 0; y < array.length; y++) {
        // create and append elements
        var tempoModuleDiv = document.createElement("div");
        tempoModuleDiv.setAttribute("id", array[y]);
        tempoModuleDiv.setAttribute("class", "loadProgress greyd");
        tempoDiv.appendChild(tempoModuleDiv);
      };
      // Putting elements inside each other
      // tempoP.appendChild(tempoNode);
      // tempoDiv.appendChild(tempoP);
      linkA.appendChild(linkNode);
      linkDiv.appendChild(linkA);
      titleP.appendChild(titleNode);
      titleDiv.appendChild(titleP);
      sectionP.appendChild(sectionNode);
      sectionDiv.appendChild(sectionP);
      mainDiv.appendChild(sectionDiv);
      mainDiv.appendChild(titleDiv);
      mainDiv.appendChild(tempoDiv);
      mainDiv.appendChild(linkDiv);

      // adding to html
      var element = document.getElementById("progress-module-container");
      element.appendChild(mainDiv);
    };

    };
};

findObjects(names);
